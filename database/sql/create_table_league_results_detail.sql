CREATE TABLE league_results_match (
  matches_id varchar(9),
  matche_no int(1),
  a_name1 varchar(10),
  a_name2 varchar(10),
  b_name1 varchar(10),
  b_name2 varchar(10),
  a_get_game_num int(1),
  b_get_game_num int(1),
  a_score1 int(2),
  b_score1 int(2),
  a_score2 int(2),
  b_score2 int(2),
  a_score3 int(2),
  b_score3 int(2),
  a_is_mc int(1),
  b_is_mc int(1),
  remarks varchar(50),
  PRIMARY KEY(matches_id, matche_no)
);
